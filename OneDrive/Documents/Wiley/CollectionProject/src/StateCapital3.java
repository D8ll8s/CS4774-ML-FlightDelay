public class StateCapital3 {

    private int population;
    private String name;

    private double areaSqMile;

    public StateCapital3(String name, int population, double areaSqMile) {
        this.population = population;
        this.name = name;
        this.areaSqMile = areaSqMile;
    }


    public int getPopulation() {
        return population;
    }

    public String getName() {
        return name;
    }

    public double getAreaSqMile() {
        return areaSqMile;
    }
}
